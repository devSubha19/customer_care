{
    protected $data;

    public function __construct($data)
    {
        $this->data = $data;
    }

    public function collection()
    {
        return collect($this->data);
    }

    public function headings(): array
    {
        // Customize the headings based on your choice
        return [
            'Column1',
            'Column2',
            // Add more columns as needed
        ];
    }
}
